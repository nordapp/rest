package org.nordapp.srv.boot.util;

public class Println {

	/**
	 * Prints out the String
	 * 
	 * @param x
	 */
	public static final void console(String x) {
		System.out.println(x);
	}

	/**
	 * Prints out the Object
	 * 
	 * @param x
	 */
	public static final void console(Object x) {
		System.out.println(x);
	}

	/**
	 * Prints out the double
	 * 
	 * @param x
	 */
	public static final void console(double x) {
		System.out.println(x);
	}

	/**
	 * Prints out the float
	 * 
	 * @param x
	 */
	public static final void console(float x) {
		System.out.println(x);
	}

	/**
	 * Prints out the long
	 * 
	 * @param x
	 */
	public static final void console(long x) {
		System.out.println(x);
	}

	/**
	 * Prints out the int
	 * 
	 * @param x
	 */
	public static final void console(int x) {
		System.out.println(x);
	}

	/**
	 * Prints out the boolean x
	 * 
	 * @param x
	 */
	public static final void console(boolean x) {
		System.out.println(x);
	}

	/**
	 * Prints out the char x
	 * 
	 * @param x
	 */
	public static final void console(char x) {
		System.out.println(x);
	}

	/**
	 * Prints out the char array x
	 * 
	 * @param x
	 */
	public static final void console(char[] x) {
		System.out.println(x);
	}
}
