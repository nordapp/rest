package org.i3xx.util.rna.core;

public class XMLException extends Exception {

    private static final long serialVersionUID = -7283223273054791367L;

	public XMLException(){super();}
    
    public XMLException(String msg){super(msg);}
    
    public XMLException(Throwable cause){super(cause);}
    
    public XMLException(String s, Throwable cause){super(s, cause);}
}
